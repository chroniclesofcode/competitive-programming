constexpr int INF = (int)1e9 + 100; 
constexpr LL LINF = std::numeric_limits<LL>::max() / 2;
constexpr int NINF = -INF;
constexpr int MX = 2 * 1e5 + 1;
constexpr int MD = (int)1e9 + 7;

int n, m, k, q;

class SegtreePNode {
public:
    LL n = 0;
    
    struct node {
        LL x;
        node() : x{LINF} {}
        node(LL e) : x{e} {}

        friend node operator+(const node &a, const node &b) {
            return node(min(a.x, b.x));
        }
    };

    vector<node> t;

    SegtreePNode(LL sz) : n(sz), t(4 * sz + 1) {}

    node query(LL l, LL r, LL v = 1, LL tl = 0, LL tr = -1) {
        if (tr == -1) tr = n - 1;
        if (l > r) {
            return node();
        }
        if (l == tl && r == tr) {
            return t[v];
        }
        LL tm = (tl + tr) / 2;
        return query(l, min(r, tm), v * 2, tl, tm) +
               query(max(l, tm + 1), r, v * 2 + 1, tm + 1, tr);
    }

    void update(LL pos, LL new_val, LL v = 1, LL tl = 0, LL tr = -1) {
        if (tr == -1) tr = n - 1;
        if (tl == tr) {
            t[v] = new_val;
        } else {
            LL tm = (tl + tr) / 2;
            if (pos <= tm)
                update(pos, new_val, v * 2, tl, tm);
            else
                update(pos, new_val, v * 2 + 1, tm + 1, tr);
            t[v] = t[v * 2] + t[v * 2 + 1];
        }
    }
};
void solve() {
    cin >> n >> q;
    vector<int> a(n);
    SegtreePNode down(n), up(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
        down.update(i, a[i] - i);
        up.update(i, a[i] + i);
    }
    for (int i = 0; i < q; i++) {
        int op, x; cin >> op;
        if (op == 1) {
            cin >> k >> x;
            k--;
            down.update(k, x - k);
            up.update(k, x + k);
        } else {
            cin >> k;
            k--;
            int left = down.query(0, k).x + k;
            int right = up.query(k, n-1).x - k;
            cout << min(left, right) << '\n';
        }
    }
}

